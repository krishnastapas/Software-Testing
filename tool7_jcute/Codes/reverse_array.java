import cute.Cute;

public class reverse_array {
    public static void main(String[] args) {
        int size = generateRandomSize();

        int[] array = generateRandomArray(size);

        System.out.println("Original Array:");
        printArray(array);

        reverseArray(array);

        System.out.println("Reversed Array:");
        printArray(array);
    }

    // Method to generate a random size for the array
    public static int generateRandomSize() {
        return (int) (Math.random() * 10) + 1; // Generates a random size between 1 and 10
    }

    // Method to generate a random array of integers
    public static int[] generateRandomArray(int size) {
        int[] array = new int[size];
        for (int i = 0; i < size; i++) {
            array[i] = (int) (Math.random() * 100); // Generates a random integer between 0 and 99
        }
        return array;
    }

    // Method to reverse the array
    public static void reverseArray(int[] arr) {
        int start = 0;
        int end = arr.length - 1;
        while (start < end) {
            int temp = arr[start];
            arr[start] = arr[end];
            arr[end] = temp;
            start++;
            end--;
        }
    }

    // Method to print the array
    public static void printArray(int[] arr) {
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] + " ");
        }
        System.out.println();
    }
}
